package cn.itcast.day07;

public class array {

        private int age;
        private  String name;

    public array() {
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public array(int age, String name) {

        this.age = age;
        this.name = name;

    }
}
