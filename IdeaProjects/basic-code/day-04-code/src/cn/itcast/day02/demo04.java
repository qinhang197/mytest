package cn.itcast.day02;

public class demo04 {
    public static void main(String[] args) {
        int a = 10, b = 11;
        boolean a1= a%2==0;
        boolean b1=b%2==0;
        System.out.println("10是偶数?"+a1);
        System.out.println("11是偶数?"+b1);
        int i=12,j=13;
        boolean i1=i%2==0;
        boolean j1=j%2==0;
        System.out.println("12是奇数?"+i1);
        System.out.println("13是奇数?"+j1);
    }
}

